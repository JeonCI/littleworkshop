package kr.co.littleworkshop.model;

public class FdImages {
	private int fdImageCode;
	private int fdCode;
	private String fdImageUuid;
	private String fdImageName;
	
	public int getFdImageCode() {
		return fdImageCode;
	}
	
	public void setFdImageCode(int fdImageCode) {
		this.fdImageCode = fdImageCode;
	}
	
	public int getFdCode() {
		return fdCode;
	}
	
	public void setFdCode(int fdCode) {
		this.fdCode = fdCode;
	}
	
	public String getFdImageUuid() {
		return fdImageUuid;
	}
	
	public void setFdImageUuid(String fdImageUuid) {
		this.fdImageUuid = fdImageUuid;
	}
	
	public String getFdImageName() {
		return fdImageName;
	}
	
	public void setFdImageName(String fdImageName) {
		this.fdImageName = fdImageName;
	}
}
